    private void sendBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendBActionPerformed
        if(!checkConnected()){
            chatta.append("Connection Lost!! :(\n");
            sendB.setEnabled(false);
            Nametf.setEnabled(false);
            msgta.setEnabled(false);
        } else {
            String msg = Nametf.getText() + "\n" + msgta.getText();
            Client.sendMsg(msg);
            chatta.append("To " + Nametf.getText() + " : " + msgta.getText() + "\n");
            msgta.setText("");
            msgta.requestFocus();
        }
    }//GEN-LAST:event_sendBActionPerformed

    private void connectbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_connectbActionPerformed
        if(!isConnected){
            HOST = Hosttf.getText();
            String temp_name = thname.getText();
            if(temp_name == null || temp_name.equals("")){
                chatta.append("Improper username\n");
                System.out.println("Inavalid username");
                return;
            }
            try{
                PORT = Integer.parseInt(Porttf.getText());
                Connect();
                sendB.setEnabled(true);
                Nametf.setEnabled(true);
                msgta.setEnabled(true);
                this.getRootPane().setDefaultButton(sendB);
                Nametf.requestFocus();
            } catch (NumberFormatException e){
                System.out.println("Invalid port no.");
                chatta.append("Improper port " + Porttf.getText() + "\n");
                Porttf.requestFocus();
                return;
            } catch (IOException ex) {
                System.out.println(ex);
                chatta.append("Not found " +HOST + ":" + PORT + "\n");
                return;
            }
        }
    }//GEN-LAST:event_connectbActionPerformed

