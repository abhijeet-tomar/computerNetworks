class chatClient implements Runnable{
    Socket SOCK;
    ClientGUI GUI;
    PrintWriter OUT;
    Scanner sc;
    
    chatClient(Socket SOCK, ClientGUI GUI) throws IOException{
        this.SOCK = SOCK;
        this.GUI = GUI;
    }
    @Override
    public void run() {
        try {
            //        throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
            OUT = new PrintWriter(SOCK.getOutputStream());
            sc = new Scanner(SOCK.getInputStream());
        } catch (IOException ex) {
            Logger.getLogger(chatClient.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("Cannot set input and output streams");
        }
        OUT.println(GUI.thname.getText());
        OUT.println(GUI.thname.getText());
        OUT.println("Welcome");
        OUT.flush();
        String inp;
        while(true){
            if(sc.hasNext()){
                inp = sc.nextLine();
                System.out.println(inp);
                GUI.chatta.append(inp + "\n");
            }
        }
    }
    public void sendMsg(String msg){
//        String msg = GUI.thname.getText() + "\n" + GUI.Nametf.getText() + " : " + GUI.msgta.getText();
        OUT.println(msg);
        OUT.flush();
    }
    
}
